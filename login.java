    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // LOGIN
       try{
         Class.forName("com.mysql.jdbc.Driver");
         Connection con=DriverManager.getConnection(  
                "jdbc:mysql://localhost:3306/management","root","");  
         
         String email = user.getText();
         String password = pass.getText(); 
         String role= "";
         
         Statement stmt=con.createStatement();  
         String sql = "select * from employee where EmpId='"+ email+"' and password='"+password+"'";
         ResultSet rs = stmt.executeQuery(sql);
         
         while(rs.next()){
             email=rs.getString(2);
             password=rs.getString(4);
             role=rs.getString(5);
             
         if(email.equals(email)&&password.equals(password)&&role.equals("employee")){
              JOptionPane.showMessageDialog(this, "Login successfully as an employee!");
               putLog();
             employee d = new employee();
             dispose();
             d.show();
            
         }if(email.equals(email)&&password.equals(password)&&role.equals("admin")){
               JOptionPane.showMessageDialog(this, "Login successfully as an admin!");
                putLog();
             admin d = new admin();
             dispose();
             d.show();
            
         }
         else{
             JOptionPane.showMessageDialog(this, "username or either password is inputed wrong");
             
             user.setText("");
             pass.setText("");
         }   
         
     }
        }
     
     catch(Exception e) {
         System.out.println(e.getMessage());
     }
          
    }//GEN-LAST:event_jLabel6MouseClicked

