    public void putLog(){
            try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-dd-MM");
            SimpleDateFormat d = new SimpleDateFormat("hh:mm a");
            Date dateobj = new Date();
            String time = d.format(dateobj.getTime());
            String today = sdf.format(dateobj.getTime());
            String date = today + " " + time;

            String query1 = "INSERT INTO `log`(`EmpId`, `password`, `date`) VALUES (?,?,?)";
            st = CNX.getConnection().prepareStatement(query1);
            st.setString(1, user.getText());
            st.setString(3, date);
            st.setString(2, pass.getText());
            if (st.executeUpdate() != 0) {
                JOptionPane.showMessageDialog(null, "Added to Log!");
            } else {
                JOptionPane.showMessageDialog(null, "Error: Check Your Information");
            }
        } catch (SQLException ex) {
            Logger.getLogger(admin.class.getName()).log(Level.SEVERE, null, ex);

        }
    }
    
